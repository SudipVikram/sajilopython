
from sajilopython import cat, dog, parrot, elephant, rabbit, fish, lion, zebra, turtle, owl
from sajilopython import cow, horse, monkey, fox, pigeon, swan, dolphin, snake
from sajilopython import background, draw

# Load a preset background
background.load("sunshine")

# Characters performing their unique actions
cat.meow()
dog.bark()
parrot.fly()
elephant.spray_water()
rabbit.hop()
fish.swim()
lion.roar()
zebra.gallop()
turtle.crawl()
owl.hoot()
cow.moo()
horse.neigh()
monkey.swing()
fox.sneak()
pigeon.glide()
swan.float()
dolphin.jump()
snake.slither()
snake.hiss()

# Drawing with the draw tools
draw.line((100, 100), (200, 200))
draw.rect((50, 50), 100, 50)
